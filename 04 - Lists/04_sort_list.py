chai = ['masala chai', 'adrak wali chai', 'nimbu wali chai']
# Sort list - List objects have a sort() method that will lost list alphanumerically, ascending by default

chai.sort()
print(chai) # alphabetic sort

my_nums = [23, 234,13, 76, 9, 43]
my_nums.sort() # numeric sort
print(my_nums)

# Sort Descending
my_nums.sort(reverse = True) # to sort descending, use keyword reverse = True
print(my_nums)

# Reverse List - reverse() method reverse the current sorting order of the elements 
chai.reverse() 
print(chai)

